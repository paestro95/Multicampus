package j211227;


public class Ex_4 {
    public static void main(String[] args) {
        // 변수 num을 만들어주세요.
        int num;
        // num에 100을 넣어주세요.
        // num 출력 : 100
        num = 100;
        System.out.println(num);
        // num에 num의 두배인 값을 넣어주세요.
        // num 출력 : 200
        num = num * 2;
        System.out.println(num);
        // num에 num에서 num을 더한 값을 넣어주세요.
        // num 출력 : 400
        num = num + num;
        System.out.println(num);
    }
}
