package j211230.Method;

public class Ex_2 {
    public static void main(String[] args) {

        hi(); // 안녕하세요. 저는 홍길동입니다. 잘부탁드립니다.

        bye(); // 안녕히가세요. 다음에 또 봐요!
    }

    static void hi() {
        System.out.println("안녕하세요. 저는 홍길동입니다. 잘부탁드립니다.");
    }

    static void bye() {
        System.out.println("안녕히가세요. 다음에 또 봐요!");
    }
}